                                Vụ Trung Quốc bắt giữ tàu lặn không người lái (UUV) của Mỹ đánh dấu nấc leo thang mới trong những nỗ lực ngăn cản các hoạt động do thám hải quân của Washington từ phía Bắc Kinh.
