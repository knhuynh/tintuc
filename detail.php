                                <li><a>Thu tàu lặn của Mỹ ở Biển Đông, Trung Quốc phát tín hiệu gì?</a></li>
                            </ul>
                            <div class="content-detail">
                                <img src="http://media.baotintuc.vn/2016/12/17/14/46/004.jpg" />
                                <p>Tờ "Wall Street Journal" có bài viết đưa ra quan điểm trên. Bên cạnh đó, vụ việc này có thể phản ánh những quan ngại ngày càng tăng của Trung Quốc trước việc Mỹ theo dõi tàu ngầm của Bắc Kinh.</p><br>

                                <p>Chuyên gia về an ninh hàng hải thuộc Viện Công nghệ Massachusetts, ông M. Taylor Fravel cho rằng loại tàu lặn này có thể giúp Hải quân Mỹ theo dõi tàu ngầm của Trung Quốc, do đó có thể có liên quan trực tiếp đến cuộc chiến chống tàu ngầm.</p><br>

                                <p>Trong những năm gần đây, Bắc Kinh đã nhanh chóng mở rộng hạm đội tàu ngầm, khiến Washington phải tăng cường các nỗ lực do thám, trong khi Trung Quốc tìm cách lẩn trốn các hệ thống phát hiện tàu ngầm của Mỹ.</p><br>

                                <p>Theo nhiều quan chức Mỹ, các tàu ngầm tấn công của Trung Quốc được triển khai sâu vào hai vùng biển Ấn Độ Dương và Thái Bình Dương. Các quan chức này dự đoán Bắc Kinh sẽ sớm bắt đầu tiến hành tuần tra bằng tàu ngầm có trang bị tên lửa hạt nhân, nhiều khả năng nhất là tại Biển Đông.</p><br>

                                <p>Trong khi đó, giám đốc Chương trình an ninh quốc tế thuộc Viện Lowy ở Sydney (Australia), ông Euan Graham cho rằng mục đích của Trung Quốc trong vụ việc trên là gieo rắc sự bất đồng hơn nữa giữa Mỹ và Tổng thống Philippines Rodrigo Duterte.</p><br>

                                <p>Kể từ khi nhậm chức, ông Duterte đã lảng tránh Washington và tỏ ý muốn xích lại gần hơn với Bắc Kinh. Thời gian qua, các quan chức Philippines bớt chỉ trích những hoạt động của Bắc Kinh trên Biển Đông, mặc dù tuần qua một số người đã bày tỏ quan ngại sau khi một cơ quan tư vấn của Mỹ cung cấp nhiều hình ảnh chụp vệ tinh cho thấy Bắc Kinh đã triển khai máy bay và một số vũ khí khác trên các đảo nhân tạo trên Biển Đông.</p>
